package com.rd.Lesson6.Conditions;

public class switchExample {
    public static void main(String[] args) {
        int degisken = 1;

        switch (degisken) {
            case 1:
                System.out.println("değişken'in değeri 1");
                break;
            case 2:
                System.out.println("değişken'in değeri 2");
                break;
            default:
                System.out.println("değişken'in değeri 1 veya 2 değil");
        }


    }
}
