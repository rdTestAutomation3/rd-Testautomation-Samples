package com.rd.Lesson6.Operators;

public class assignment {
    public static void main(String[] args) {
        int x = 10;

        x += 5; // x artık 15
        System.out.println(x);

        x -= 3; // x artık 12
        System.out.println(x);

        x *= 2; // x artık 24
        System.out.println(x);

        x /= 4; // x artık 6
        System.out.println(x);

        x %= 5; // x artık 1
        System.out.println(x);
    }
}
