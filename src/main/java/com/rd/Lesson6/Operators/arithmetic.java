package com.rd.Lesson6.Operators;

public class arithmetic {
    public static void main(String[] args) {
        int x = 10;
        int y = 3;
        int toplam = x + y; // toplam = 13
        System.out.println("x + y= "+toplam);
        int fark = x - y;   // fark = 7
        System.out.println("x - y= "+fark);
        int carpim = x * y; // carpim = 30
        System.out.println("x * y= "+carpim);
        float bolum = x / y;  // bolum = 3
        System.out.println("x / y= "+bolum);
        int kalan = x % y;  // kalan = 1
        System.out.println(" x % y= "+kalan);

    }
}
